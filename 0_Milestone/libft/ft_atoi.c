#include "libft.h"

int	ft_atoi(const char *nptr)
{
	size_t	i;
	int		isnegative;
	int		result;

	result = 0;
	isnegative = 1;
	i = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			isnegative = -1 * isnegative;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		result = (result * 10) + (nptr[i] - '0');
		i++;
	}
	return (result * isnegative);
}
